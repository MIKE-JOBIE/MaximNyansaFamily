                    <h3>THE SOFTWARE ENGINEERING CURRICULUM</h3>
                    <p>The software Engineering training has the goal of getting newbies (aspiring software developers) and intermediate developers to get started well with their journey and hopefully be prepared for the job market after completing all modules
                        successfully. Software development is a relatively green field in Africa and has a lot of opportunities for young graduates to apply their creative knowledge to solve world problems. There are a lot of different specializations
                        in the software world such as Mobile Application development, Web development, Desktop Applications development, etc.</p>
                    <p>In our programme module, we focus more on web development and give brief introductions to the other different specialization areas which helps trainees to have a broader view of the field and decide for themselves where they want to
                        focus in their career life. In the final month of the programme, we brainstorm ideas on a potential product that can be developed by the team and we work on it in groups of developers.</p>
                    <p>The programme will include the following modules:</p>
                    <strong>1. General Introduction to software Engineering</strong> <br/>

                    <strong>2. Setting up development environment</strong> <br/>

                    <strong>3. Software Engineering collaboration tools</strong><br/>

                    <strong>4. Technologies and concepts in web development</strong><br/>

                    <strong>5. Databases</strong><br/>

                    <strong>6. Web application frameworks</strong> <br/>

                    <strong>7 Web development with Laravel framework (A php web application framework)</strong><br/>

                    <strong>8. Servers and Hosting </strong><br/>

                    <strong>9. Real life web application project</strong> <br/>

                    <br>
                    <p>During the programme practical assignments will constantly be executed and in the final month a real life project will be introduced to work on by the team, preferably a project that can be continued to benefit the world after completion.</p>

